    <h4 class="card-title"><Center><b><u>¿Cómo descargar el Programa?</Center></b></u></h4>
    <div class="col" style="padding:5px; background-color: #F0FFF0; padding-right:0px; padding-left:0px;"></div>
<p class="card-text">El inventario se transmitirá mediante un archivo '.sln' lo cual puede llegar a necesitar un software especial, por ejemplo software Windows Installer XML Solution. Sin embargo, no es la única manera de realizarlo Si no tiene el software en el que se creó su archivo SLN en su PC, puede abrirlo estableciendo sus asociaciones de archivos predeterminadas para abrir archivos SLN en un programa similar, como como Asterisk Native Data Format, o Visual Studio .NET Solution, o cualquier otro software que use archivos SLN. <br>
    <BR>
    File Magic es un visor de software universal que puede abrir la mayoría de las extensiones de archivos, incluidos algunos archivos binarios SLN. Al descargar el Visor de software universal de File Magic, ahora puede abrir archivos incluso cuando no tenga el software. Le ayudará a ahorrar recursos funcionales en su PC, ya que no tiene tantos programas en ejecución y por lo tanto reduce los conflictos de software. Lo más importante es que no tendrá que gastar dinero en la compra de software costoso que solo puede usar una vez para ver su archivo SLN.</p>
